public class Menu {
    private Boolean iniciar;
    private Boolean sair;

    public Boolean getIniciar() {
        return iniciar;
    }

    public void setIniciar(Boolean iniciar) {
        this.iniciar = iniciar;
    }

    public Boolean getSair() {
        return sair;
    }

    public void setSair(Boolean sair) {
        this.sair = sair;
    }
}